#include "../includes/libft.h"

int		ft_strisdigit(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
		if ((str[i] < '0' || str[i] > '9') && str[i] != '-' && str[i] != '+')
			return (0);
	return (1);
}
