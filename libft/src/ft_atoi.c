#include "../includes/libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int ans;

	i = 0;
	sign = 1;
	ans = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		ans = 10 * ans + (str[i] - '0');
		i++;
	}
	return (ans * sign);
}
