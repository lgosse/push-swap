#include "../includes/libft.h"

int		ft_strisalpha(char *str)
{
	int i;

	i = -1;
	while (str[++i] != '\0')
		if (!(str[i] >= 'a' && str[i] <= 'z') &&
			!(str[i] >= 'A' && str[i] <= 'Z'))
			return (0);
	return (1);
}
